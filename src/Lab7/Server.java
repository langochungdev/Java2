    void start(){
        new Thread(()-> {
            try {
                ServerSocket ss =  new ServerSocket(9998);
                sk = ss.accept(); 
                txtMain.setText("connected");
                Thread t = new Thread(Server.this);
                t.start();
            }catch(IOException e){}
        }).start();
    }
    
    void sendMes(){
        try {
            DataOutputStream ops = new DataOutputStream(sk.getOutputStream());// cổng 
            ops.writeUTF(txtSend.getText()); // mã hóa 
        }catch(Exception e){}
    }
    
    @Override
    public void run(){
        try(DataInputStream ips = new DataInputStream(sk.getInputStream())) {
            while(true){
                String ms = ips.readUTF();
                txtMain.setText(ms);
            }
        }catch(IOException e){}
    }
                
