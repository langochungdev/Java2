    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        final String username = txtEmail.getText();
        final String password = txtPassword.getText();

        Properties prop = new Properties();
        prop.put("mail.smtp.host", "smtp.gmail.com");
        prop.put("mail.smtp.port", "465");
        prop.put("mail.smtp.auth", "true");
        prop.put("mail.smtp.socketFactory.port", "465");
        prop.put("mail.smtp.socketFactory.class", "javax.net.ssl.SSLSocketFactory");
       
        Session session = Session.getInstance(prop,
                new javax.mail.Authenticator() {
                    protected PasswordAuthentication getPasswordAuthentication() {
                        return new PasswordAuthentication(username, password);
                    }
                });

        try {
            Message m = new MimeMessage(session);
            m.setFrom(new InternetAddress(txtEmail.getText()));
            
            m.setRecipients(
                Message.RecipientType.TO,
                InternetAddress.parse(txtEmailTo.getText())
            );
            if(!txtEmail.getText().isEmpty()){
                m.setRecipients(
                Message.RecipientType.CC,
                InternetAddress.parse(txtEmailCC.getText())  // Địa chỉ CC
                );
            }
            
            m.setSubject(txtSubject.getText());
            m.setText(txtMessage.getText());
            
            MimeMultipart mmp = new MimeMultipart();
            
            MimeBodyPart messBP = new MimeBodyPart();
            messBP.setText(txtMessage.getText());
            mmp.addBodyPart(messBP);
            
            if(!txtFile.getText().isEmpty()){
                MimeBodyPart fileBP = new MimeBodyPart(); //file
                String path = txtFile.getText();
                File file = new File(path);
            
                DataSource sc = new FileDataSource(file);
                fileBP.setDataHandler(new DataHandler(sc));
                fileBP.setFileName(file.getName());
                mmp.addBodyPart(fileBP);
            }
            
            m.setContent(mmp);
                    
            Transport.send(m);
            JOptionPane.showMessageDialog(this, "Done");
        }catch(MessagingException e){
            System.out.println(e);
        }
    }//GEN-LAST:event_btnSendActionPerformed

