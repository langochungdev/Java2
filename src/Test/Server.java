    void sk1(){
        new Thread(()->{
            try {
                ServerSocket ss =  new ServerSocket(Integer.valueOf(txtPort.getText()));
                sk1 = ss.accept(); 
                loadText("Connected k1");
                getMess(sk1, "sk1");
            }catch(IOException e){}
        }).start();
    }
    
    void sk2(){
        new Thread(()->{
            try {
                ServerSocket ss =  new ServerSocket(Integer.valueOf(txtPort.getText()) + 1);
                sk2 = ss.accept(); 
                loadText("Connected k2");
                getMess(sk2, "sk2");
            }catch(IOException e){}
        }).start();
    }
    
    void getMess(Socket sk, String name){
        new Thread(() -> {
            try(DataInputStream ips = new DataInputStream(sk.getInputStream())) {
                while(true){
                    String m = ips.readUTF();
                    loadText(name+": "+m);
                }
            }catch(IOException e){}
        }).start();
    }
    
    void loadText(String m){
        temp += m+"\n";
        txtMain.setText(temp);
    }

