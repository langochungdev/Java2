    public List<Book> findByTitle(String title){
        List<Book> dsTimKiem = new ArrayList<>();  
        String sql = "select id, title, price from BOOKS where title like ? ";
        try {
            Connection conn = DBConnect.getConnection();
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, '%'+title+'%');
            ResultSet rs = ps.executeQuery();
            
            while(rs.next()){
                Book b = new Book();
                b.setID(rs.getInt("id"));
                b.setTitle(rs.getString("title"));
                b.setPrice(rs.getFloat("price"));
                dsTimKiem.add(b);
            }
            return dsTimKiem;
        }catch(Exception e){
            return null; 
        }
    }
    
    public int deleteByID(int id){
        String sql = "delete from Books where id = ? ";
        try {
            Connection conn = DBConnect.getConnection();
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setInt(1, id);
            
            int rs = ps.executeUpdate();
            return rs;
        }catch(Exception e){
            return 0;
        }
    }
            
    public void fillTable(String title){
        DefaultTableModel tbModel = (DefaultTableModel) tbDanhSach.getModel();
        dsBook = findByTitle(title);
        tbModel.setRowCount(0);
        for(Book b: dsBook){
            tbModel.addRow(b.toRow());
        }
    }
